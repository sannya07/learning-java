package DAY1;

public class While {
    public static void main(String[] args){
        int count = 1;
        // when udk how many times loop should run
        while(count!=5){
            System.out.println(count);
            count++;
        }
        // when uk how many times loop should run
        for(int i=0;i<5;i++){
            System.out.println(count);
        }
    }
}
